namespace App\Controller;

use Cake\Controller\Component\AuthComponent;
use Cake\Controller\Controller;
use Cake\Event\Event;
use Composer\Cache;

/**
 * Application Controller
 *
 * Add your application-wide methods in the class below, your controllers
 * will inherit them.
 *
 * @property AppController $this
 *
 * @link https://book.cakephp.org/3.0/en/controllers.html#the-app-controller
 */
class AppController extends Controller
{

    /**
     * @throws \Exception
     */
    public function initialize()
    {
        parent::initialize();

        \Cake\Cache\Cache::disable();

        \Cake\I18n\FrozenTime::setDefaultLocale('fr-Fr');
        \Cake\I18n\FrozenTime::setToStringFormat('dd/mm/YYYY H:mm:ss');
        \Cake\I18n\FrozenTime::setJsonEncodeFormat('dd/mm/YYYY H:mm:ss');

        $this->loadComponent('RequestHandler');
        $this->loadComponent('Flash');

        $this->loadComponent('Auth', [
            'authenticate' => [
                'Form' => [
                    'fields' => [
                        'username' => 'email',
                        'password' => 'password'
                    ]
                ],
            ],
            'loginAction' => [
                'controller' => 'Users',
                'action' => 'login'
            ],
            // Si pas autorisé, on renvoit sur la page précédente
            //'unauthorizedRedirect' => $this->referer()
        ]);

        // Nom du controller var_dump($this->getName());
    }

    protected function renderToJson(string $json, $code=200)
    {
        return $this->response
            ->withStatus($code)
            ->withType('application/json')
            ->withStringBody($json);
    }

    public function beforeFilter(Event $event)
    {
        $this->getEventManager()->off($this->Csrf);
        return parent::beforeFilter($event); // TODO: Change the autogenerated stub
    }
}
